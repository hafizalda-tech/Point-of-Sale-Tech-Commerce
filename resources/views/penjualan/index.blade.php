<script>
    let table, table1;

    $(function () {
        table = $('.table-penjualan').DataTable({
            responsive: true,
            processing: true,
            serverSide: true,
            autoWidth: false,
            ajax: {
                url: '{{ route('penjualan.data_filtered', [$tanggalAwal, $tanggalAkhir]) }}',
            },
            columns: [
                {data: 'DT_RowIndex', searchable: false, sortable: false},
                {data: 'tanggal'},
                {data: 'kode_member'},
                {data: 'total_item'},
                {data: 'total_harga'},
                {data: 'diskon'},
                {data: 'bayar'},
                {data: 'kasir'},
                {data: 'aksi', searchable: false, sortable: false},
            ]
        });

        table1 = $('.table-detail').DataTable({
            processing: true,
            autoWidth: false,
            bSort: false,
            dom: 'Brt',
            columns: [
                { data: 'DT_RowIndex', width: '5%', searchable: false, sortable: false },
                { data: 'kode_produk', width: '8%' },
                { data: 'nama_produk', width: '15%' },
                { data: 'merk', width: '8%' },
                { data: 'harga_jual', width: '10%' },
                { data: 'jumlah', width: '6%' },
                { data: 'subtotal', width: '10%' },
                { data: 'harga_beli', width: '12%' },
                { data: 'hpp', width: '10%' },
                { data: 'laba_kotor', width: '10%' }
            ]

        });

        $('.datepicker').datepicker({
            format: 'yyyy-mm-dd',
            autoclose: true,
            todayHighlight: true
        });
        
        $('#modal-form').on('shown.bs.modal', function () {
            $('.datepicker').datepicker('update');
        });
    });

    function updatePeriode() {
        $('#modal-form').modal('show');
    }

    function showDetail(url) {
        $('#modal-detail').modal('show');

        table1.ajax.url(url);
        table1.ajax.reload();
    }

    function deleteData(url) {
        if (confirm('Yakin ingin menghapus data terpilih?')) {
            $.post(url, {
                    '_token': $('[name=csrf-token]').attr('content'),
                    '_method': 'delete'
                })
                .done((response) => {
                    table.ajax.reload();
                })
                .fail((errors) => {
                    alert('Tidak dapat menghapus data');
                    return;
                });
        }
    }
</script>
